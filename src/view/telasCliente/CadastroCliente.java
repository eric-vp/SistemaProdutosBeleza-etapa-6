    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
        Utils utils = new Utils();
        int opcao = utils.confirmar("Deseja mesmo limpar todos os campos?");
        
        if (opcao == JOptionPane.YES_OPTION) {
            limparCampos();        
        }
    }//GEN-LAST:event_btnLimparActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome = txtNome.getText();
        String dataNascimentoStr = txtDataNascimento.getText();
        String email = txtEmail.getText();
        String cpf = txtCpf.getText();
        String endereco = txtEndereco.getText();
        String telefone = txtTelefone.getText();   
        
        ClienteController controller = new ClienteController();
        
        boolean sucesso = controller.cadastrarCliente(nome, dataNascimentoStr, email, cpf, endereco, telefone);
        
        if (sucesso) {
            limparCampos();
        }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

